const STANDINGS = STANDINGS_YEARS[CURRENT_YEAR];
const CONFERENCE_STANDINGS = CONFERENCE_STANDINGS_YEARS[CURRENT_YEAR];
const REPLACEMENTS = [
  ["Mich.", "Michigan"],
  ["St.", "State"],
  ["U.", "University"],
  ["UConn", "Connecticut"],
];

function getCHNName(teamName) {
  for (let [value, replacement] of REPLACEMENTS) {
    if (teamName.endsWith(value)) {
      return teamName.replace(value, replacement);
    }
  }
  return teamName;
}

export function getStandingsForTeam(team) {
  const chnName = getCHNName(team.name);

  let stats = STANDINGS[chnName];
  let conferenceStats = CONFERENCE_STANDINGS[chnName];
  if (!stats || !conferenceStats) {
    console.log("NO STATS FOR", team, chnName);
    return null;
  }

  return { ...stats, ...conferenceStats };
}
